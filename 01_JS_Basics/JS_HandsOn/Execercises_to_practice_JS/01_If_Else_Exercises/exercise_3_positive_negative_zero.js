// Positive, Negative, or Zero
// Write a program that checks if a number is positive, negative, or zero and prints the result. Hint: Use if, else if, and else to cover all cases.

let n = 8;
if (n > 0) {
    console.log("n is positive");
} else if (n < 0) {
    console.log("n is negative");
} else {
    console.log("n is zero");
}