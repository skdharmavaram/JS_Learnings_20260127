// Age Group Classifier
// Create a program that takes a person’s age as input and prints whether they are a "Child" (0-12), "Teen" (13-19), or "Adult" (20+). Hint: Use multiple if-else conditions to check age ranges.


let age = 22;
if (age <= 9) {
    console.log("You are a child.");
} else if (age < 18) {
    console.log("You are a teenager.");
} else {
    console.log("You are an adult.");
}
