//3 ways of declaring the variables in JS
// Using var
var playerName = "Sachin";
var country = "India";
console.log("Using var - Player: " + playerName + ", Country: " + country);

// Using let   
let age = 50;
let city = "Mumbai";
console.log(`Using let - Age: ${age}, City: ${city}`);

// Using const
const sports = "Cricket";
const birthYear = 1973;
console.log(`Using const - Sports: ${sports}, Birth Year: ${birthYear}`);